const payButton1 = document.getElementById("payButton1").onclick = function(){
    const thirdPartyRadio = document.getElementById("thirdParty1");
    const bankRadio = document.getElementById("bank1");

    if (thirdPartyRadio.checked) {
      alert("Redirecting to third-party payment gateway...");
    } else if (bankRadio.checked) {
      alert("Payment successful!");
    } else {
      alert("Please select a payment method.");
    }
}

